<p style="font-size: 20px;">
1. Summer (March to June) – Ideal for sightseeing, adventure sports, and escaping the summer heat.<br><br>
2. Monsoon (July to September) – Lush greenery but prone to landslides; best for those who love solitude and rain.<br><br>
3. Winter (October to February) – A snowy paradise for skiing, snowboarding, and enjoying a winter wonderland.</h2>
